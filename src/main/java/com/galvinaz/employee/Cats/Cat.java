package com.galvinaz.employee.Cats;

public class Cat {

    String names;
    String color;


    public Cat(String names, String color) {
        this.names = names;
        this.color = color;
    }

    public String getNames() {
        return names;
    }

    public String getColor() {
        return color;
    }

    public void setNames(String names) {
        this.names = names;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
